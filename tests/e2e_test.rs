#[test]
fn e2e_reachability_phantom_dep_not_imported() {
    let tmp = setup_project(
        &cargo_lock_with_dep("octocrab", "0.39.0"),
        &[("main.rs", "use serde::Serialize;\nfn main() {}\n")],
    );
    let evidence = check_reachability(tmp.path(), "octocrab")
        .expect("check_reachability must succeed");
    assert!(!evidence.is_imported, "octocrab is not imported — must be phantom");
    assert_eq!(evidence.status, ReachabilityStatus::Phantom);
    assert!(evidence.import_sites.is_empty());
}

#[test]
fn e2e_reachability_imported_dep_is_reachable() {
    let tmp = setup_project(
        &cargo_lock_with_dep("serde", "1.0.200"),
        &[("lib.rs", "use serde::Serialize;\n\npub struct Foo;\n")],
    );
    let evidence = check_reachability(tmp.path(), "serde")
        .expect("check_reachability must succeed");
    assert!(evidence.is_imported, "serde is imported — must be reachable");
    assert_eq!(evidence.status, ReachabilityStatus::Reachable);
    assert!(!evidence.import_sites.is_empty());
}

