/// Build a synthetic Vulnerability struct for testing.
fn make_vuln(package: &str, version: &str, has_fix: bool, semver: bool) -> Vulnerability {
    Vulnerability {
        id: format!("RUSTSEC-2026-{:04}", package.len()),
        cve: Some("CVE-2026-99999".to_string()),
        summary: format!("Test advisory for {}", package),
        package: package.to_string(),
        version: version.to_string(),
        severity: Some(8.0),
        severity_label: SeverityLabel::High,
        fixed_versions: if has_fix {
            vec!["2.0.0".to_string()]
        } else {
            vec![]
        },
        semver_fix_available: semver,
        source_tier: SourceTier::Tier1,
        references: vec!["https://rustsec.org/advisories/RUSTSEC-2026-0001.html".to_string()],
    }
}

/// Create a temporary project directory with a Cargo.lock and optional src/ files.
fn setup_project(cargo_lock_content: &str, src_files: &[(&str, &str)]) -> TempDir {
    let tmp = TempDir::new().expect("Failed to create temp dir");
    std::fs::write(tmp.path().join("Cargo.lock"), cargo_lock_content)
        .expect("Failed to write Cargo.lock");

    if !src_files.is_empty() {
        let src_dir = tmp.path().join("src");
        std::fs::create_dir_all(&src_dir).expect("Failed to create src dir");
        for (name, content) in src_files {
            std::fs::write(src_dir.join(name), content).expect("Failed to write src file");
        }
    }
    tmp
}

/// Minimal valid Cargo.lock containing a single registry dependency.
fn cargo_lock_with_dep(name: &str, version: &str) -> String {
    format!(
        "# This file is automatically @generated by Cargo.\nversion = 4\n\n\
         [[package]]\nname = \"{}\"\nversion = \"{}\"\n\
         source = \"registry+https://github.com/rust-lang/crates.io-index\"\n\
         checksum = \"abc123\"\n",
        name, version
    )
}

// ---------------------------------------------------------------------------
