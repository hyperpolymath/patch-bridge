#[test]
fn e2e_full_pipeline_lockfile_to_classification() {
    // Simulate: parse lockfile → determine dep → classify as phantom (not imported)
    let tmp = setup_project(
        &cargo_lock_with_dep("orphaned", "0.5.0"),
        &[("main.rs", "// No imports of orphaned here\nfn main() {}\n")],
    );
    let deps = parse_cargo_lock(&tmp.path().join("Cargo.lock"))
        .expect("lockfile parse must succeed");
    assert!(!deps.is_empty(), "Must find orphaned dep in lockfile");

    let dep = &deps[0];
    let evidence = check_reachability(tmp.path(), &dep.name)
        .expect("reachability check must succeed");
    assert_eq!(evidence.status, ReachabilityStatus::Phantom, "Unimported dep must be phantom");

    let vuln = make_vuln(&dep.name, &dep.version, false, false);
    let (cls, _rationale, _action) = classify(&vuln, &evidence);
    assert_eq!(cls, Classification::Informational, "Phantom dep must classify as Informational");
}
