// ---------------------------------------------------------------------------

#[test]
fn e2e_lockfile_parse_single_registry_dep() {
    let tmp = setup_project(&cargo_lock_with_dep("serde", "1.0.200"), &[]);
    let deps = parse_cargo_lock(&tmp.path().join("Cargo.lock"))
        .expect("parse_cargo_lock must succeed");
    assert_eq!(deps.len(), 1, "Expected exactly one dependency");
    assert_eq!(deps[0].name, "serde");
    assert_eq!(deps[0].version, "1.0.200");
    assert_eq!(deps[0].ecosystem, "crates.io");
}

#[test]
fn e2e_lockfile_parse_multiple_deps() {
    let lock = format!(
        "# Generated by Cargo.\nversion = 4\n\n{}{}",
        cargo_lock_with_dep("serde", "1.0.200"),
        cargo_lock_with_dep("anyhow", "1.0.86"),
    );
    let tmp = setup_project(&lock, &[]);
    let deps = parse_cargo_lock(&tmp.path().join("Cargo.lock"))
        .expect("parse_cargo_lock must succeed");
    assert_eq!(deps.len(), 2, "Expected two dependencies");
}

#[test]
fn e2e_lockfile_skips_local_packages_without_source() {
    let lock = "[[package]]\nname = \"my-project\"\nversion = \"0.1.0\"\n";
    let tmp = setup_project(lock, &[]);
    let deps = parse_cargo_lock(&tmp.path().join("Cargo.lock"))
        .expect("parse_cargo_lock must succeed");
    assert!(deps.is_empty(), "Local packages with no registry source must be skipped");
}

